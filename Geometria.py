import math
def areaCuadrado(n):
        return n * n


def areaCirculo(r):
        PI = 3.1416
        return PI * pow(r, 2)

def areaTriangulo(a, b):
        return (a * b)/2

def areaRectangulo(a, b):
        return a * b

def areaPentagono(p, a):
        return (p * a)/2

def areaRombo(d, d_):
        return (d * d_)/2

def areaRomboide(b, h):
        return b * h

def areaTrapecio(b, b_, h):
        return ((b + b_)/2)*h